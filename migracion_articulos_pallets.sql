-- 1. Tabla de artículos
CREATE TABLE IF NOT EXISTS articulos (
  id                INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  empresa_id        INT UNSIGNED NOT NULL,
  proveedor_id      INT UNSIGNED NULL,
  codigo            VARCHAR(30)  NOT NULL,
  descripcion       VARCHAR(200) NOT NULL,
  presentacion      VARCHAR(100) NOT NULL DEFAULT '',
  unidades_por_caja DECIMAL(10,2) NULL     COMMENT 'Unidades por caja/display',
  bultos_por_pallet DECIMAL(10,2) NOT NULL DEFAULT 1,
  activo            TINYINT(1)   NOT NULL DEFAULT 1,
  creado_en         DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (empresa_id)   REFERENCES empresas(id),
  FOREIGN KEY (proveedor_id) REFERENCES proveedores(id),
  INDEX idx_art_codigo (empresa_id, codigo)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 2. Pallets reales en remitos (editable por operador)
